users = {
    "Олег": "6-12",
    "Олександра": "14-18",
    "Наталі": "19-40",
    "Петро": "41-60"
}

name = input("Введіть ім'я користувача: ")

try:
    age_group = users[name]
    print(f"Вікова група користувача {name}: {age_group}")
except KeyError:
    print("Користувача з таким ім'ям не знайдено.")
